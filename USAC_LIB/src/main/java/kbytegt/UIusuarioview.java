    private void btn_passwordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_passwordActionPerformed
        // BOTON CAMBIAR CONTRASE√ëA
        String password = JOptionPane.showInputDialog(this, "Ingresa tu nueva contrase√±a","Nueva contrase√±a",JOptionPane.WARNING_MESSAGE);
        new_password = usac_lib.security.getMD5(password);
        this.btn_password.setText("Contrase√±a cambiada");
        flag_password = true;
    }//GEN-LAST:event_btn_passwordActionPerformed

    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        // ELIMINAR CUENTA
        int respuesta = JOptionPane.showConfirmDialog(this, "¬øEst√°s seguro de eliminar tu cuenta?","Cuenta",JOptionPane.YES_NO_OPTION,JOptionPane.WARNING_MESSAGE);
        
        if(respuesta == JOptionPane.YES_OPTION){
            //Eliminar cuenta
            System.out.println(" Respuesta el JOptionPane = "+respuesta);
            usac_lib.usuarios.eliminar(temp_carnet);
            usac_lib.LogOut();
            this.dispose();
        }
    }//GEN-LAST:event_btn_eliminarActionPerformed

    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        // GUARDAR CAMBIOS
         NodoUsuario usuario;
        if(flag_password){
            usuario = new NodoUsuario(temp_carnet,in_nombre.getText(),in_apellido.getText(),in_carrera.getText(),new_password);
        } else {
            usuario = new NodoUsuario(temp_carnet,in_nombre.getText(),in_apellido.getText(),in_carrera.getText(),old_password);
        }
        System.out.println(" [Editar]Carnet del usuario: " + usuario.getCarnet());
        usac_lib.usuarios.insertar(usuario);
        usac_lib.reLogIn();
        this.dispose();
    }//GEN-LAST:event_btn_guardarActionPerformed

